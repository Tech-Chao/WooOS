import Foundation

/// Defines in which order the array of requested objects is listed: Ascending or decending.
///
/// - ascending: List objects in an ascending order.
/// - descending: List objects in a descending order.
public enum WooSortOrder: String {
    case ascending = "asc"
    case descending = "desc"
}
